    // remplire les champs
    private void DeplacerStagiaire(int i){
        try{
            if(i == 0){
                btnPrecedent.setEnabled(false);
                btnSuivant.setEnabled(true);
            }
            else if(i == dt.getRowCount() -1){
                btnPrecedent.setEnabled(true);
                btnSuivant.setEnabled(false); 
            }
            else{
                btnPrecedent.setEnabled(true);
                btnSuivant.setEnabled(true);
            }
            txtmat.setText(dt.getValueAt(i, 0).toString());
            txtnom.setText(dt.getValueAt(i, 1).toString());
            txtprenom.setText(dt.getValueAt(i, 2).toString());
            txtmoyenne.setText(dt.getValueAt(i, 3).toString());
            txtdateNaiss.setText(dt.getValueAt(i, 4).toString());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erreur de deplacement stagaire\n"+ e.getMessage());

        }
    }
    // procedure 
    private void AfficherStagaire(){
        try{
           dt.setRowCount(0);
           Rs = St.executeQuery("Select * from stg");
           
           while(Rs.next()){
               mat = Rs.getInt("mat");
               nom = Rs.getString("nom");
               prenom = Rs.getString("prenom");
               moyenne = Rs.getDouble("moyenne");
               dateNaiss = Rs.getDate("dateNaiss");
               
               Object[] stg = {mat,nom,prenom,moyenne,dateNaiss};
               dt.addRow(stg);
           }
           
        }
        catch(Exception s){
            JOptionPane.showMessageDialog(null, "Erreur d'affichage stagaire\n"+ s.getMessage());
 
        }
    }
