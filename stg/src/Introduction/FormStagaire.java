    private void btnRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercheActionPerformed
        // TODO add your handling code here:
        
        try{
           dt.setRowCount(0);
           if(jRadioButton1.isSelected() == true){
               String sql = "SELECT * FROM stg WHERE stg.mat = "+txtRecherche.getText()+"";
               Rs = St.executeQuery(sql);
           }
           else{
               
                String sql = "SELECT * FROM stg WHERE stg.nom = '"+txtRecherche.getText()+"'";
                Rs = St.executeQuery(sql);
                
           }   
           
           while(Rs.next()){
               mat = Rs.getInt(1);
               nom = Rs.getString(2);
               prenom = Rs.getString(3);
               moyenne = Rs.getDouble(4);
               dateNaiss = Rs.getDate(5);
               
               Object[] stg = {mat,nom,prenom,moyenne,dateNaiss};
               dt.addRow(stg);
           }
           
           if(dt.getRowCount() == 0){
               JOptionPane.showMessageDialog(null,"Pas de stg trouve");
           }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erreur de recherche stagaire\n"+ e.getMessage());

        }

    }//GEN-LAST:event_btnRechercheActionPerformed

